import type { Vec3 } from '../sim/geo';

// Компонент: летящая боеголовка — от команды пуска до момента детонации.
export type Warhead = {
  yield: number; // мощность заряда в мегатоннах (1 | 10 | 100)
  seed: number; // сид для детерминированных эффектов взрыва (рендер/визуал)
  t: number; // время, прошедшее с момента пуска, сек
  flightTime: number; // полное время полёта до детонации, сек
  dir: Vec3; // точка прицеливания на единичной сфере
};

// Компонент: активная волна взрыва (визуализация/тайминг — используется рендер-слоем, Task 8+).
export type Blast = {
  age: number; // время с момента детонации, сек
  yield: number;
  seed: number;
  ts: number; // временной масштаб волны (см. CasualtySystem)
};

// Сущность ECS-мира симуляции.
export type Entity = {
  onSphere?: Vec3; // точка на поверхности сферы (для сущностей, привязанных к поверхности)
  warhead?: Warhead;
  blast?: Blast;
};
