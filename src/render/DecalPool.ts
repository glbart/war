// Чистая (без three.js) логика кольцевого пула декалей-кратеров — вынесена из DecalView,
// чтобы поведение "растим лениво до лимита, затем переиспользуем самый старый слот" было
// юнит-тестируемо без моков three. Тот же приём, что и SlotPool.ts для MissileView/ExplosionView,
// но здесь пул не про "первый свободный", а про "самый старый" (кратеры не освобождаются сами —
// они постоянны, пока их место не понадобится новому взрыву).
export class RingCursor {
  // Монотонный счётчик всех вызовов next() с последнего reset(); индекс слота — его остаток
  // от деления на capacity. Пока total < capacity, idx строго растёт 0,1,2,... — ровно
  // порядок появления новых слотов. После первого полного круга idx снова идёт 0,1,2,... —
  // то есть переписывает слоты в том же порядке, в каком они были заняты впервые, а значит
  // всегда возвращает индекс слота, записанного раньше всех (FIFO по кругу).
  private total = 0;

  constructor(private readonly capacity: number) {}

  // Индекс слота для очередного spawn(). Вызывающая сторона трактует idx === текущая длина
  // пула как "нужно завести новый слот", иначе — "переиспользовать существующий по этому индексу".
  next(): number {
    const idx = this.total % this.capacity;
    this.total += 1;
    return idx;
  }

  // Сбрасывает счётчик — используется на planetReset, чтобы пул снова заполнялся с нуля
  // в исходном порядке (семантически все декали уже сочтены "удалёнными").
  reset(): void {
    this.total = 0;
  }
}
